// organización.js

/** Clase que representa una organización */

class Organizacion {
  constructor(name, address, phone, representative, email) {
    this.name = name; // nombre de la organización
    this.address = address; // dirección
    this.phone = phone; // número de telefono
    this.representative = representative; // representante de la organización
    this.email = email; // correo electrónico
  }
}
